package com.sino.base.system.entity;

public class SnmpInfo {
	private Double sortKey;
	private String snmpKey;
	private String snmpVal;
	
	public Double getSortKey() {
		return sortKey;
	}
	public void setSortKey(Double sortKey) {
		this.sortKey = sortKey;
	}
	public String getSnmpKey() {
		return snmpKey;
	}
	public void setSnmpKey(String snmpKey) {
		this.snmpKey = snmpKey;
	}
	public String getSnmpVal() {
		return snmpVal;
	}
	public void setSnmpVal(String snmpVal) {
		this.snmpVal = snmpVal;
	}
}
